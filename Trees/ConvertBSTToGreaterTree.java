package Trees;

public class ConvertBSTToGreaterTree {
    static int sum=0;
    private static TreeNode convertBST(TreeNode root) {
        if (root != null) {
            convertBST(root.right);
            sum += root.val;
            root.val = sum;
            convertBST(root.left);
        }
        return root;
    }
}
